package org.songpro.models;

public class Part {
  private String chord;
  private String lyric;

  public String getChord() {
    return chord;
  }

  public String getLyric() {
    return lyric;
  }

  public void setChord(String chord) {
    this.chord = chord;
  }

  public void setLyric(String lyric) {
    this.lyric = lyric;
  }
}
